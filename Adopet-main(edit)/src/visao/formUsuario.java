    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(txtUsuario.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o nome para continuar");
            txtUsuario.requestFocus();
        }else if(senha.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha a senha  para continuar");
            senha.requestFocus();
        }else if(confSenha.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha a confirma√ß√£o de senha para continuar");
            confSenha.requestFocus();
        }else 
         if (senha.getText().equals(confSenha.getText())){
            if(flag==1){
            mod.setUsuNome(txtUsuario.getText());
            mod.setUsuTipo((String)jComboBox1.getSelectedItem());
            mod.setUsuSenha(senha.getText());
            dao.Salvar(mod);
            txtUsuario.setText("");
            senha.setText("");
            confSenha.setText("");
            txtUsuario.setEnabled(false);
            senha.setEnabled(false);
            jComboBox1.setEnabled(false);
            btnSalvar.setEnabled(false);
            btnNovo.setEnabled(true);
            txtId.setText("");
            btnCancelar.setEnabled(false);
             preencherTabela("select * from usuarios order by usu_nome");
        }else{
        mod.setUsuNome(txtUsuario.getText());
        mod.setUsuTipo((String)jComboBox1.getSelectedItem());
        mod.setUsuSenha(senha.getText());
        mod.setIdUsu(Integer.parseInt(txtId.getText()));
       dao.Alterar(mod);
       txtUsuario.setText("");
       senha.setText("");
       confSenha.setText("");
       txtId.setText("");
       btnNovo.setEnabled(true);
       txtUsuario.setEnabled(false);
       senha.setEnabled(false);
       jComboBox1.setEnabled(false);
       btnSalvar.setEnabled(false);
       btnCancelar.setEnabled(false);
        preencherTabela("select * from usuarios order by usu_nome");
        }
         }else{
             JOptionPane.showMessageDialog(rootPane,"Senhas n√£o correspondentes!");
         }  
    }//GEN-LAST:event_btnSalvarActionPerformed

